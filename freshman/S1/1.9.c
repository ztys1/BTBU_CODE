/*1.9 圆柱的面积*

成绩: 10 / 折扣: 0.8
从键盘输入圆柱底面的半径和高，输出圆柱的表面积，保留三位小数。圆周率以3.1415926计算。
提示：圆柱表面积=底面积*2+侧面积（侧面积是底面周长*高）*/

#include<stdio.h>
int main()
{    
  float a,h,b,c;
  double s,v;
  scanf("%f,%f\n",&a,&h);
  s=3.1415926*a*a;  
  c=2*3.1415926*a;
  v=c*h;
  b=2*s+v;
  printf("面积=%.3f\n",b);
  return 0;
}