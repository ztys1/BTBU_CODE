#include <stdio.h>

int main() {
    // 输入两个数，每个数占4位，依次为千、百、十、个位
    int a[4], b[4], c[4] = {0};
    printf("请输入第一个数的千百十个位：");
    scanf("%d%d%d%d", &a[0], &a[1], &a[2], &a[3]);
    printf("请输入第二个数的千百十个位：");
    scanf("%d%d%d%d", &b[0], &b[1], &b[2], &b[3]);

    // 逐位相加，并按照修正规则修正
    for (int i = 3; i >= 0; i--) {
        c[i] = a[i] + b[i];
        if (i < 3 && c[i+1] >= 10) {  // 低位有进位
            c[i] += 1;                // 高位加1
            c[i+1] -= 10;             // 进位减10
        }
        if (c[i] >= 10 && c[i] < 16) {
            c[i] += 6;
        } else if (c[i] >= 16) {
            c[i] += 6;
            if (i > 0) {              // 不是最高位，要考虑高位进位
                c[i-1] += 1;
            }
        }
    }

    // 输出结果
    printf("结果为：");
    for (int i = 0; i < 4; i++) {
        printf("%d", c[i]);
    }
    printf("\n");

    return 0;
}